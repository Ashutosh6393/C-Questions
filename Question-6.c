// Input the height and base of the triangle and print the area
#include<stdio.h>
void main(){
    float h,b;
    printf("\nEnter the height of the triangle : ");
    scanf("%f",&h);
    printf("\nEnter the base of the triangle : ");
    scanf("%f",&b);
    printf("\nThe area of the triangle is : %.3f",(b*h)/2);
    
}